# include "3-calc.h"


/**
 * op_add - function to add 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return:sum
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_mod - function to return the remainder
 * @a: divider
 * @b:quatient
 *
 * Return: remainder
 */

int op_mod(int a, int b)
{
return (a % b);
}

/**
 * op_sub - function to substract
 * @a: first number
 * @b: second number
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_div - function to divide
 * @a: divider
 * @b: dividend
 *
 * Return: quatient
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mul - function to return productof 2 number
 * @a: first number
 * @b: second number
 *
 * Return:product
 */

int op_mul(int a, int b)
{
return (a * b);
}
